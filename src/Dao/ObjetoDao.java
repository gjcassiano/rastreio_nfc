package Dao;

public class ObjetoDao {
	private String nuid;
	private String name;
	private String info;

	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getInfo() {
		return info;
	}


	public void setInfo(String info) {
		this.info = info;
	}


	public String getNuid() {
		return nuid;
	}
	

	public void setNuid(String nuid) {
		this.nuid = nuid;
	}
	
}
